// vetores: conjunto/lista de coleções do mesmo tipo
// vetores ou array
// colchetes armazenam a coleção

let notas = [7.5, 6.3, 4.8, 9.2];
console.log("Notas Iniciais", notas);

// Ordenar em crescente

notas.sort((a, b) => a - b);

console.log("Notas Ordenadas (Crescente)", notas);

// Ordenar em decrescente

notas.sort((a, b) => b - a);

console.log("Notas Ordenadas (Decrescente)", notas);

// vetor de string

let frutas = ["banana", "maça", "laranja", "abacaxi"];

console.log("Coleção de frutas", frutas);

// Ordem Alfabetica

frutas.sort();
console.log("Frutas Ordenadas:", frutas);






